public class Lesson_3_Data_Type {
  public static void main(String random[]) {
    float marks = 98.5f;
    // In java char data type use UNICODE instead of ASCII. And the size of a char
    // is 2 bytes. It suppports all the languages
    // Int has 4 different types. int, long, short and byte
    // byte has 1 byte size, short has 2 byte size, long has 8 byte size, int has 4
    // byte size
    long l = 1L; // Unlike float,long has to be written with L at the end
    System.out.println(marks);
    System.out.println(l);
    boolean run = true;
    System.out.println(run);
  }
}
