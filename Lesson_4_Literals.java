public class Lesson_4_Literals {
  public static void main(String args[]) {
    // Literals
    int num1 = 12; // Works fine
    int num2 = 0b1100; // Binary literal
    int num3 = 0x0C; // Hexadecimal literal
    int num4 = 12_34_56; // Underscores are allowed
    System.out.println(num1);
    System.out.println(num2);
    System.out.println(num3);
    System.out.println(num4);
  }
}
